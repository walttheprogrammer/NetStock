private double obtener_valor_en(JTable tabla, int fila, int columna) {
    Object val = tabla.getValueAt(fila, columna);
    if (val == null) return 0.0; //si es 0, solo devuelve 0, para que no se reemplace todo el rato 
    try {
        return Double.parseDouble(val.toString());
    } catch (Exception e) {  //y si existe excepción porque aún no se agrega nada, devuelve 0.0
        return 0.0;
    }
}
    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed
        try {    
        DefaultTableModel modelo = (DefaultTableModel) tabla_p.getModel();     
            String precio_del_producto = txtPrecio.getText();
            String producto = cc_productos.getSelectedItem().toString();
            Object[] tabla_cmp_fila = {producto, precio_del_producto};
            modelo.addRow(tabla_cmp_fila);    //si existe algún valor, se agrega este valor a la tabla
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "No se puede agregar un valor.", "Error", JOptionPane.ERROR_MESSAGE);                    
           }
        cc_productos.repaint();
        cc_productos.revalidate();
    }//GEN-LAST:event_btn_agregarActionPerformed
    public void sumarxcolumna() {
       double sum = 0.0;  //esta función permite sumar desde la segunda columna, para que nos de un total de lo que se va comprar
    for (int i = 0; i < tabla_p.getRowCount(); i++) {
        sum += obtener_valor_en(tabla_p, i, 1); // 1 = columna del precio del producto 
    }
        txtMontototal.setText(String.valueOf(sum));
    }
