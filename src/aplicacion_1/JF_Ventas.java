    private void btn_limpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_limpiarActionPerformed
        try {    
        DefaultTableModel modelo = (DefaultTableModel) tabla_p.getModel();     
        for (int i = modelo.getRowCount() - 1; i >= 0; i--) {
                modelo.removeRow(i);
        }    //si existen las filas o fila correspondientes, se procede a eliminar esa fila o todas las filas que existan
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "No se puede limpiar la tabla. Verifique que al menos exista una fila", "Error", JOptionPane.ERROR_MESSAGE);                    
           }
        cc_productos.repaint();
        cc_productos.revalidate();
    }//GEN-LAST:event_btn_limpiarActionPerformed
